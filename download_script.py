# Output folder
output_dir = "downloaded_images"
os.makedirs(output_dir, exist_ok=True)

# Download each group
for group_index, group in enumerate(image_groups, start=1):
    for image_index, url in enumerate(group, start=1):
        file_ext = os.path.splitext(url.split("?")[0])[1] or ".jpg"  # Handle query strings, fallback to .jpg
        filename = f"product-{group_index}-image-{image_index}{file_ext}"
        filepath = os.path.join(output_dir, filename)
        try:
            response = requests.get(url, timeout=10)
            response.raise_for_status()
            with open(filepath, "wb") as f:
                f.write(response.content)
            print(f"Downloaded: {filename}")
        except Exception as e:
            print(f"Failed to download {url}: {e}")
